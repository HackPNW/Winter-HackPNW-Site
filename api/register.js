const yup = require("yup");
const _ = require("lodash");
const { MongoClient } = require("mongodb");
const nodemailer = require("nodemailer");
const Turnstile = require("cf-turnstile");

const turnstile = Turnstile(process.env.TURNSTILE_KEY);

const url = process.env.MONGO_URL;
const client = new MongoClient(url);

const dbName = "hackpnw";

const colorOptions = ["red", "amber", "green", "cyan", "violet", "pink"];
const gradeOptions = ["9", "10", "11", "12"];

const formSchema = yup.object({
  firstName: yup.string().required().label("First Name"),
  lastName: yup.string().required().label("Last Name"),
  email: yup.string().required().email().label("Email"),
  school: yup.string().required().label("High School"),
  grade: yup
    .string()
    .oneOf(gradeOptions, "Grade cannot be empty")
    .label("Grade"),
  color: yup
    .string()
    .oneOf(colorOptions, "Favourite Color cannot be empty")
    .label("Favourite Color"),
  github: yup.string().matches(/^[a-z\d](?:[a-z\d]|-(?=[a-z\d])){0,38}$/i, {
    message: "GitHub username is not valid",
    excludeEmptyString: true,
  }),
  createTeam: yup.bool().required(),
  hasTeamCode: yup
    .bool()
    .when("createTeam", { is: false, then: (x) => x.required() }),
  teamCode: yup
    .string()
    .label("Team Code")
    .when(["createTeam", "hasTeamCode"], {
      is: (createTeam, hasTeamCode) => !createTeam && hasTeamCode,
      then: (x) =>
        x.required().matches(/^\d{4}-\d{4}$/, "Team Code is not valid"), // TODO: backend check
    }),
  teamName: yup
    .string()
    .label("Team Name")
    .when("createTeam", {
      is: true,
      then: (x) =>
        x
          .required()
          .matches(
            /^[a-z\d ]*$/i,
            "Team name must only contain: letters, numbers and spaces"
          )
          .min(5)
          .max(20),
    }),
  fillTeam: yup.bool().when("createTeam", {
    is: true,
    then: (x) => x.required(),
  }),
  restrictions: yup.string(),
});

async function createTeam(teamName, creatorId, fillTeam, db) {
  const collection = db.collection("teams");

  let duplicate = "empty";
  let code = "";

  while (duplicate != null) {
    code = Math.floor(Math.random() * 1_0000_0000)
      .toString()
      .padStart(8, "0");
    code = code.slice(0, 4) + "-" + code.slice(4, 8);

    duplicate = await db.collection("teams").findOne({ code: code });
  }

  const doc = {
    name: teamName,
    creatorId: creatorId,
    fillTeam: fillTeam,
    code,
  };

  await collection.insertOne(doc);

  return doc;
}

export default async function handler(request, response) {
  if (request.method !== "POST")
    return response.status(405).send("Method not allowed");

  // Checks
  let form = request.body;
  console.log(form);

  const turnstileResult = await turnstile(form.tsToken, {
    remoteip: request.headers["x-forwarded-for"],
  });
  if (!turnstileResult.success) {
    return response
      .status(400)
      .send("Invalid token.\nPlease try submitting again.");
  }

  try {
    form = await formSchema.validate(form);
  } catch (e) {
    console.log(":(");
    console.log(e);
    return response.status(400).send(e);
  }

  await client.connect();
  const db = client.db(dbName);
  const collection = db.collection("registrations");

  const doc = _.pick(form, [
    "firstName",
    "lastName",
    "email",
    "school",
    "grade",
    "color",
    "github",
    "restrictions",
  ]);

  if (form.createTeam || !form.hasTeamCode) {
    return response.status(400).send("Unable to sign up without team code");
  }

  let teamCode = null;
  if (form.createTeam) {
    const teamDoc = await createTeam(form.teamName, null, form.fillTeam, db);
    console.log(teamDoc);
    doc.teamId = teamDoc._id;
    teamCode = teamDoc.code;
  } else if (form.hasTeamCode) {
    const teamDoc = await db
      .collection("teams")
      .findOne({ code: form.teamCode });
    if (!teamDoc) return response.status(400).send("Not valid team code");
    doc.teamId = teamDoc._id;

    const sizeOfTeam = (
      await db
        .collection("registrations")
        .find({ teamId: doc.teamId })
        .toArray()
    ).length;

    if (sizeOfTeam >= 4) return response.status(400).send("Team is full");
  } else {
    doc.teamId = null;
  }

  await collection.insertOne(doc);

  console.log(doc);

  // try {
  //   await db
  //     .collection("teams")
  //     .updateOne({ _id: doc.teamId }, { creatorId: doc._id });
  // } catch (e) {
  //   console.log(e);
  // }

  if (teamCode != null)
    await sendEmail(
      form.email,
      "Team Name: " + form.teamName,
      "Invite Code: " + teamCode,
      "Registration Info"
    );
  else await sendEmail(form.email, " ", " ", " ");

  response.status(200).json({
    success: true,
    teamCode,
  });
}

